    public void loadStudentId() {
       try
        {
        
        ArrayList<String> studentIdList=PerformenceDao.getAllStudentId();
        if(studentIdList.isEmpty()==true)
          JOptionPane.showMessageDialog(null, "Sorry! No student has yet given any exam","No Records!",JOptionPane.INFORMATION_MESSAGE);  
    else
        {
            jComboBox1.removeAllItems();
            for(String sid: studentIdList)
                 jComboBox1.addItem(sid);
        }
                
        }
        catch(SQLException ex)
        {
          JOptionPane.showMessageDialog(null, "Error while connecting to DB!","Exception!",JOptionPane.ERROR_MESSAGE);
          ex.printStackTrace();  
        }
    }
   
    
public void loadUI(){
        jTextField1.setText(UserProfile.getUsername());
        isAdding=true;
        jComboBox2.removeAllItems();
        jComboBox2.addItem("ExamId");
        for(String exid: examIdList)
                 jComboBox2.addItem(exid);
      isAdding=false;  
    }


