    private void jComboBox2ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox2ItemStateChanged
        // TODO add your handling code here:
     if(evt.getStateChange()==ItemEvent.DESELECTED)
          return;
      String studentId=jComboBox1.getSelectedItem().toString(); 
      String examId=jComboBox2.getSelectedItem().toString();

      try
      {
        StudentScorePojo scoreObj=PerformenceDao.getScore(studentId, examId);
        jTextField3.setText(scoreObj.getLanguage());
        jTextField4.setText(String.valueOf(scoreObj.getPer()));
      }
      catch(SQLException ex)
        {
          JOptionPane.showMessageDialog(null, "Error while connecting to DB!","Exception!",JOptionPane.ERROR_MESSAGE);
          ex.printStackTrace();  
        }
      

    }//GEN-LAST:event_jComboBox2ItemStateChanged

    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        // TODO add your handling code here:
                 
        if(evt.getStateChange()==ItemEvent.DESELECTED)
          return;
        String studentId=jComboBox1.getSelectedItem().toString(); 
        try
        {
        ArrayList<String> examIdList=PerformenceDao.getAllExamId(studentId);
        
        jComboBox2.removeAllItems();
        
        for(String exid: examIdList)
                 jComboBox2.addItem(exid);
       
        }
        
        catch(SQLException ex)
        {
          JOptionPane.showMessageDialog(null, "Error while connecting to DB!","Exception!",JOptionPane.ERROR_MESSAGE);
          ex.printStackTrace();  
        }
    

    }//GEN-LAST:event_jComboBox1ItemStateChanged

