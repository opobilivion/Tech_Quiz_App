     public boolean validateInput()
       {
           question=jTextArea1.getText().trim();
           option1=jTextField2.getText().trim();
           option2=jTextField3.getText().trim();
           option3=jTextField4.getText().trim();
           option4=jTextField5.getText().trim();
           correctOption=jComboBox1.getSelectedItem().toString();
           if(question.isEmpty() || option1.isEmpty() || option2.isEmpty()||option3.isEmpty()||option4.isEmpty()||correctOption.isEmpty())
           {
               
               return false;
           } 
           else 
               return true;
       }
     public void clearAll()
     {   jTextArea1.setText("");
         jTextField2.setText("");
         jTextField3.setText("");
         jTextField4.setText("");
         jTextField5.setText("");
         jComboBox1.setSelectedIndex(0);
     }
    
