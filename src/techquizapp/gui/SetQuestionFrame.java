    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       if(validateInput()==false)
       {
           JOptionPane.showMessageDialog(null,"Please Fill all the Input fileds!","Error",JOptionPane.ERROR_MESSAGE);
           return;
       }
       correctOption=jComboBox1.getSelectedItem().toString();
       String optionName=options.get(correctOption);  // Yha pe Correct Option optionName se mil jayega..aur fir use pass karenge.
       QuestionPojo obj=new QuestionPojo(newExam.getExamId(),qno,newExam.getLanguage(),option1,option2,option3,option4,optionName,question);
       qstore.addQuestion(obj);
       clearAll();
       int ans=(newExam.getTotalQuestions())- qno;
       LblTitle.setText(ans+"Questions Remaining");
       qno++;
       if(qno>newExam.getTotalQuestions())
       {
           disableAll();
       }
       
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(qno<=newExam.getTotalQuestions())
        {  int remainingQuestions=newExam.getTotalQuestions()-qno+1;
           JOptionPane.showMessageDialog(null,"You have still"+remainingQuestions+"Remaining,You Cane not Close!","Error!",JOptionPane.ERROR_MESSAGE);
           return;
        }
       try{
           ExamDao.addExam(newExam);
           QuestionDao.addQuestion(qstore);
           JOptionPane.showMessageDialog(null,"Your Question has Been set Successfull!","EXAM ADDED",JOptionPane.INFORMATION_MESSAGE);
           new AdminsOptionsFrame().setVisible(true);
           this.dispose();
       }
       catch(SQLException ex)
       {
           
           JOptionPane.showMessageDialog(null,"Error Occured While adding to DB!","DB ERROR",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
       }
    }//GEN-LAST:event_jButton3ActionPerformed

